// Função para envolver a primeira palavra em um span
function wrapFirstWord(text) {
  const words = text.split(" ");
  if (words.length > 1) {
    return `<span>${words[0]}</span> ${words.slice(1).join(" ")}`;
  }
  return text;
}

// Função para atualizar o idioma
function setLanguage(lang) {
  // Atualizar o atributo lang da tag <html>
  document.documentElement.lang = lang;

  // Atualizar todos os elementos com data-i18n
  document.querySelectorAll("[data-i18n]").forEach(element => {
    const key = element.getAttribute("data-i18n");
    if (translations[key] && translations[key][lang]) {
      // Aplicar span na primeira palavra para títulos específicos
      if (["about-title", "portfolio-title", "brands-title", "contact-title"].includes(key)) {
        element.innerHTML = wrapFirstWord(translations[key][lang]);
      } else {
        element.innerHTML = translations[key][lang];
      }
    }
  });

  // Atualizar placeholders traduziveis
  document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (translations[key] && translations[key][lang]) {
      element.setAttribute("placeholder", translations[key][lang]);
    }
  });

  // Atualizar atributos aria-label traduziveis
  document.querySelectorAll("[data-i18n-aria-label]").forEach(element => {
    const key = element.getAttribute("data-i18n-aria-label");
    if (translations[key] && translations[key][lang]) {
      element.setAttribute("aria-label", translations[key][lang]);
    }
  });

  // Atualizar o título da página
  const titleKey = document.querySelector("title").getAttribute("data-i18n");
  if (translations[titleKey] && translations[titleKey][lang]) {
    document.title = translations[titleKey][lang];
  }

  // Atualizar o efeito de digitação
  startTypingEffect(lang);

  // Atualizar o toggle do idioma
  const flagImages = {
    pt: "/images/bandeira-pt.png",
    en: "/images/bandeira-en.png",
    es: "/images/bandeira-es.png"
  };
  const langNames = {
    pt: "PT",
    en: "EN",
    es: "ES"
  };
  const flagAlts = {
    pt: "Bandeira de Portugal",
    en: "Flag of the United Kingdom",
    es: "Bandera de Espana"
  };
  document.getElementById("current-flag").src = flagImages[lang];
  document.getElementById("current-flag").alt = flagAlts[lang];
  document.getElementById("current-lang").textContent = langNames[lang];

  // Salvar a escolha do idioma no localStorage
  localStorage.setItem("language", lang);
}

// Função para o efeito de digitação
function startTypingEffect(lang) {
  const typingElement = document.getElementById("typing-text");
  const text = translations["typing-text"][lang];
  let index = 0;

  // Limpar o texto atual e reiniciar o índice
  typingElement.innerHTML = "";
  clearTimeout(window.typingTimeout);

  function typeEffect() {
    if (index < text.length) {
      typingElement.innerHTML += text.charAt(index);
      index++;
      window.typingTimeout = setTimeout(typeEffect, 60);
    }
  }

  typeEffect();
}

// Funcionalidades do site
document.addEventListener("DOMContentLoaded", () => {
  // Inicializar o idioma
  const savedLang = localStorage.getItem("language") || "pt";
  setLanguage(savedLang);

  // LIGHTBOX
  const lightboxOverlay = document.getElementById("lightbox-overlay");
  const lightboxImg    = document.getElementById("lightbox-img");
  const lightboxClose  = document.getElementById("lightbox-close");

  function openLightbox(src, alt) {
    lightboxImg.src = src;
    lightboxImg.alt = alt || "";
    lightboxOverlay.classList.add("open");
    document.body.style.overflow = "hidden";
  }

  function closeLightbox() {
    lightboxOverlay.classList.remove("open");
    document.body.style.overflow = "";
    lightboxImg.src = "";
  }

  document.querySelectorAll(".project img").forEach(img => {
    img.addEventListener("click", () => openLightbox(img.src, img.alt));
  });

  lightboxClose.addEventListener("click", closeLightbox);
  lightboxOverlay.addEventListener("click", e => {
    if (e.target === lightboxOverlay) closeLightbox();
  });
  document.addEventListener("keydown", e => {
    if (e.key === "Escape") closeLightbox();
  });

  // (Removido código do menu-btn/menu do topo. Apenas menu flutuante lateral permanece)

  // Evento do formulário de contato
  const whatsappNumber = "+351967578514";
  document.getElementById("contactForm").addEventListener("submit", function (event) {
    event.preventDefault();
    const message = document.getElementById("message").value.trim();
    if (!message) {
      return;
    }
    const whatsappMessage = encodeURIComponent(message);
    const whatsappLink = `https://wa.me/${whatsappNumber}?text=${whatsappMessage}`;
    window.open(whatsappLink, "_blank");
  });

  // Busca e filtro de projetos
  const projectCards = Array.from(document.querySelectorAll(".project"));
  const searchInput = document.getElementById("project-search");
  const filterButtons = Array.from(document.querySelectorAll(".filter-btn"));
  const emptyState = document.getElementById("projects-empty");
  let activeFilter = "all";

  function applyProjectFilters() {
    const term = (searchInput.value || "").toLowerCase().trim();
    let visibleCount = 0;

    projectCards.forEach(card => {
      const name = card.dataset.name || "";
      const tech = card.dataset.tech || "";
      const matchesFilter = activeFilter === "all" || tech.includes(activeFilter);
      const matchesTerm = !term || name.includes(term) || tech.includes(term);
      const shouldShow = matchesFilter && matchesTerm;

      card.style.display = shouldShow ? "grid" : "none";
      if (shouldShow) {
        visibleCount += 1;
      }
    });

    emptyState.hidden = visibleCount !== 0;
  }

  searchInput.addEventListener("input", applyProjectFilters);

  filterButtons.forEach(button => {
    button.addEventListener("click", () => {
      activeFilter = button.dataset.filter || "all";
      filterButtons.forEach(item => item.classList.remove("active"));
      button.classList.add("active");
      applyProjectFilters();
    });
  });

  applyProjectFilters();

  // Duplicação da seção de marcas
  const brands = document.querySelector(".brands");
  const clone = brands.innerHTML;
  brands.innerHTML += clone;

  // Navegacao rapida entre secoes
  const sectionDock = document.querySelector(".section-dock");
  const dockToggle = document.querySelector(".dock-toggle");
  const dockLinks = Array.from(document.querySelectorAll(".dock-links a"));
  const dockTargets = dockLinks
    .map(link => document.getElementById(link.dataset.target || ""))
    .filter(Boolean);

  if (sectionDock && dockToggle && dockLinks.length) {
    dockToggle.addEventListener("click", () => {
      const isOpen = sectionDock.classList.toggle("is-open");
      dockToggle.setAttribute("aria-expanded", isOpen ? "true" : "false");
    });

    dockLinks.forEach(link => {
      link.addEventListener("click", () => {
        sectionDock.classList.remove("is-open");
        dockToggle.setAttribute("aria-expanded", "false");
      });
    });

    document.addEventListener("click", event => {
      if (!sectionDock.contains(event.target)) {
        sectionDock.classList.remove("is-open");
        dockToggle.setAttribute("aria-expanded", "false");
      }
    });

    if (dockTargets.length) {
      const observer = new IntersectionObserver(
        entries => {
          entries.forEach(entry => {
            if (!entry.isIntersecting) {
              return;
            }

            const activeId = entry.target.id;
            dockLinks.forEach(link => {
              const isActive = link.dataset.target === activeId;
              link.classList.toggle("active", isActive);
            });
          });
        },
        {
          root: null,
          threshold: 0.35,
          rootMargin: "-20% 0px -55% 0px"
        }
      );

      dockTargets.forEach(section => observer.observe(section));
    }
  }

  // Eventos do menu de idiomas
  const toggle = document.getElementById("language-toggle");
  const dropdown = document.getElementById("language-dropdown");

  // Abrir/fechar dropdown ao clicar
  toggle.addEventListener("click", () => {
    const isOpen = dropdown.classList.toggle("active");
    toggle.setAttribute("aria-expanded", isOpen);
    dropdown.setAttribute("aria-hidden", !isOpen);
  });

  // Selecionar idioma no dropdown
  dropdown.querySelectorAll("li").forEach(item => {
    item.addEventListener("click", () => {
      const lang = item.getAttribute("data-lang");
      setLanguage(lang);
      dropdown.classList.remove("active");
      toggle.setAttribute("aria-expanded", "false");
      dropdown.setAttribute("aria-hidden", "true");
    });
  });

  // Suporte a teclado (Enter ou Espaço para abrir/fechar)
  toggle.addEventListener("keydown", (event) => {
    if (event.key === "Enter" || event.key === " ") {
      event.preventDefault();
      const isOpen = dropdown.classList.toggle("active");
      toggle.setAttribute("aria-expanded", isOpen);
      dropdown.setAttribute("aria-hidden", !isOpen);
    }
  });

  // Fechar dropdown ao clicar fora
  document.addEventListener("click", (event) => {
    if (!toggle.contains(event.target) && !dropdown.contains(event.target)) {
      dropdown.classList.remove("active");
      toggle.setAttribute("aria-expanded", "false");
      dropdown.setAttribute("aria-hidden", "true");
    }
  });
});